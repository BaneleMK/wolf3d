#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*s;

	i = 0;
	s = b;
	if (len > 0)
	{
		while (i <= len - 1)
		{
			s[i] = c;
			i++;
		}
	}
	return (b);
}
