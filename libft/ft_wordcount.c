#include "libft.h"

size_t	ft_wordcount(const char *s, char c)
{
	size_t	i;
	size_t	n;

	i = 0;
	n = 0;
	while (s[i])
	{
		while (s[i] == c && s[i])
			i++;
		if (s[i] != c && s[i])
		{
			n++;
			while (s[i] != c && s[i])
				i++;
		}
	}
	return (n);
}
